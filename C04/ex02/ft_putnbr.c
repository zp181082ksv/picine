void	ft_putnbr(int nb)
{
	char	number;

	if (nb == -2147483648)
		write(1, "-2147483648)", 11);
	if (nb < 0)
	{
		nb = -nb;
		write(1, "-", 1);
	}
	if (nb >= 10)
		ft_putnbr(nb / 10);
	number = nb % 10 + '0';
	write(1, &number, 1);
}
