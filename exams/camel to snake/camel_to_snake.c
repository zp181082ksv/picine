#include <unistd.h>

int	main(int an, char** av)
{
	int	i;
	char	c;
	c = '_';
	char	d;
	
	if (an ==2)
	{
		while (av[1][i])
		{
			if (av[1][i] >= 'A' && av[1][i] <= 'Z')
			{
				write(1, &c, 1);
				d = av[1][i] + 32;
				write(1, &d, 1);	
			}
			else
				write(1, &av[1][i], 1);
			i++;	
		}
				
	}
	
	write(1, "\n", 1);
	return (0);
}	
